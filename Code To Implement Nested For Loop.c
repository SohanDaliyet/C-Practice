/* C Code To Implement Nested For Loop */

#include<stdio.h>
#include<conio.h>

void main()
{
    int i,j;

    for(i=0;i<=2;i++)
    {
        for(j=0;j<=2;j++)
        {
            printf("\n The Value Of i = %d",i);
            printf("\n The Value Of j = %d",j);
        }
    }

}
